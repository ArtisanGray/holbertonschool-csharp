using System;

namespace MyMath
{
    /// <summary>
    /// Math operations class.
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// This method adds two integers.
        /// </summary>
        /// <param name="a">first parameter passed from call</param>
        /// <param name="b">second parameter passed from call</param>
        /// <returns>the sum of both these numbers.</returns>
        public static int Add(int a, int b)
        {
            return (a + b);
        }
    }
}
